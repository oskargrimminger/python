numbers = list(range(1,10))

for number in numbers:
    if (number == 1 ):
        print("\n" + str(number) + "st")
    elif (number == 2):
        print("\n" + str(number) + "nd")
    elif (number == 3):
        print("\n" + str(number) + "rd")
    else:
        print("\n" + str(number) + "th")
        